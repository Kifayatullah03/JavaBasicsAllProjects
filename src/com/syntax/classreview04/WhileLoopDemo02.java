package com.syntax.classreview04;

public class WhileLoopDemo02 {

	public static void main(String[] args) {
		
		int u = 0;
		
		while(u <10) {
			System.out.println(u);
			u++;
		}
		
		System.out.println("-----------------------------------");

		int x = 2;
		while(x<=10) {
			System.out.println(x);
			x=x+2;
		}
		
		System.out.println("------------------------------------------");
		
		int  y = 1;
		while(y<=10) {
			System.out.println(y);
			y=y+1;
		}
		
	}
}