package com.syntax.classreview04;

public class ForLoopsDemo04 {

	public static void main(String[] args) {
		
		for(int x = 1; x<=10; x++) {
			System.out.println(x);
		}
		
		System.out.println("----------------------------------------");
		
		for(int x = 9; x >= 1; x=x-2) {
			System.out.println(x);
		}
		
		
		
	}

}
